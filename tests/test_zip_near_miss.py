class TestNearMissSimulator:
    def test_output_shape(self):
        sim = NearMissSimulator(n_groups=3, seed=0)
        df = sim.simulate(n_drivers=50, n_weeks=10)
        assert df.shape[0] == 50 * 10
        expected_cols = {"driver_id", "week_id", "exposure_km", "true_group"} | set(
            _DEFAULT_EVENT_TYPES
        )
        assert expected_cols.issubset(set(df.columns))

    def test_non_negative_counts(self):
        sim = NearMissSimulator(seed=1)
        df = sim.simulate(n_drivers=30, n_weeks=8)
        for et in _DEFAULT_EVENT_TYPES:
            assert df[et].min() >= 0, f"Negative counts in {et}"

    def test_positive_exposure(self):
        sim = NearMissSimulator(seed=2)
        df = sim.simulate(n_drivers=20, n_weeks=4)
        assert df["exposure_km"].min() > 0.0

    def test_group_labels_in_range(self):
        n_groups = 3
        sim = NearMissSimulator(n_groups=n_groups, seed=3)
        df = sim.simulate(n_drivers=60, n_weeks=5)
        assert df["true_group"].min() >= 0
        assert df["true_group"].max() < n_groups

    def test_single_group(self):
        sim = NearMissSimulator(n_groups=1, seed=4)
        df = sim.simulate(n_drivers=10, n_weeks=3)
        assert df["true_group"].unique().to_list() == [0]

    def test_single_week(self):
        sim = NearMissSimulator(seed=5)
        df = sim.simulate(n_drivers=20, n_weeks=1)
        assert df.shape[0] == 20


