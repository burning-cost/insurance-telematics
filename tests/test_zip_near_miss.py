class TestPredictGroupProbs:
    def test_output_columns(self, fitted_model, weekly_data_3group):
        probs = fitted_model.predict_group_probs(weekly_data_3group)
        expected_cols = {"driver_id", "prob_group_0", "prob_group_1", "prob_group_2"}
        assert expected_cols.issubset(set(probs.columns))

    def test_probabilities_sum_to_one(self, fitted_model, weekly_data_3group):
        probs = fitted_model.predict_group_probs(weekly_data_3group)
        prob_cols = [f"prob_group_{g}" for g in range(3)]
        row_sums = sum(probs[c] for c in prob_cols)
        assert (row_sums - 1.0).abs().max() < 1e-5

    def test_output_n_rows(self, fitted_model, weekly_data_3group):
        probs = fitted_model.predict_group_probs(weekly_data_3group)
        n_drivers = weekly_data_3group.select("driver_id").unique().shape[0]
        assert probs.shape[0] == n_drivers


