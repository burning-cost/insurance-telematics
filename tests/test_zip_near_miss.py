# 8. Edge cases
# ---------------------------------------------------------------------------


class TestEdgeCases:
    def test_single_group_degenerates_to_zip(self):
        sim = NearMissSimulator(n_groups=1, seed=10)
        df = sim.simulate(n_drivers=30, n_weeks=8)
        model = ZIPNearMissModel(n_groups=1, max_iter=20, random_state=0)
        model.fit(df)
        assert model.mixing_weights_.shape == (1,)
        assert abs(float(model.mixing_weights_[0]) - 1.0) < 1e-6

    def test_single_week(self):
        sim = NearMissSimulator(n_groups=2, seed=11)
        df = sim.simulate(n_drivers=40, n_weeks=1)
        model = ZIPNearMissModel(n_groups=2, max_iter=15, random_state=0)
        model.fit(df)
        probs = model.predict_group_probs(df)
        assert probs.shape[0] == 40

    def test_missing_required_column_raises(self):
        sim = NearMissSimulator(seed=12)
        df = sim.simulate(n_drivers=10, n_weeks=4)
        df_bad = df.drop("exposure_km")
        model = ZIPNearMissModel(n_groups=2)
        with pytest.raises(ValueError, match="exposure_km"):
            model.fit(df_bad)

    def test_predict_before_fit_raises(self):
        sim = NearMissSimulator(seed=13)
        df = sim.simulate(n_drivers=10, n_weeks=4)
        model = ZIPNearMissModel(n_groups=2)
        with pytest.raises(RuntimeError, match="fit"):
            model.predict_group_probs(df)
