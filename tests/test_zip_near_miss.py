class TestZIPNearMissModelFit:
    def test_fit_returns_self(self, weekly_data_3group):
        model = ZIPNearMissModel(n_groups=3, max_iter=10, random_state=0)
        result = model.fit(weekly_data_3group)
        assert result is model

    def test_log_likelihood_non_decreasing(self, weekly_data_3group):
        """EM must not decrease the observed-data log-likelihood."""
        model = ZIPNearMissModel(n_groups=3, max_iter=30, random_state=42)
        model.fit(weekly_data_3group)
        ll = model.log_likelihood_history_
        assert len(ll) >= 2
        # Allow tiny numerical wobble but no significant decrease
        diffs = np.diff(ll)
        assert np.all(diffs >= -0.5), f"LL decreased by more than 0.5: {diffs.min():.4f}"

    def test_mixing_weights_sum_to_one(self, fitted_model):
        total = float(fitted_model.mixing_weights_.sum())
        assert abs(total - 1.0) < 1e-6, f"Mixing weights sum to {total}"

    def test_mixing_weights_non_negative(self, fitted_model):
        assert np.all(fitted_model.mixing_weights_ >= 0)

    def test_posteriors_shape(self, weekly_data_3group, fitted_model):
        n_drivers = weekly_data_3group.select("driver_id").unique().shape[0]
        assert fitted_model.group_posteriors_.shape == (n_drivers, 3)

    def test_posteriors_sum_to_one(self, fitted_model):
        row_sums = fitted_model.group_posteriors_.sum(axis=1)
        assert np.allclose(row_sums, 1.0, atol=1e-6), "Posterior rows do not sum to 1"

    def test_zip_params_all_groups_present(self, fitted_model):
        assert set(fitted_model.zip_params_.keys()) == {0, 1, 2}

    def test_zip_params_valid_values(self, fitted_model):
        for g, params in fitted_model.zip_params_.items():
            pi = params["pi"]
            lam = params["lambda_per_km"]
            assert 0.0 <= pi <= 1.0, f"Group {g} pi={pi} out of range"
            assert lam >= 0.0, f"Group {g} lambda={lam} < 0"


