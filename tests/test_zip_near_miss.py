# 6. driver_risk_features — join with aggregate_to_driver
# ---------------------------------------------------------------------------


class TestDriverRiskFeaturesJoin:
    def test_join_with_aggregate_to_driver(self, weekly_data_3group):
        """driver_risk_features() must join cleanly to aggregate_to_driver() output."""
        # Simulate trip data for the same set of drivers
        sim = TripSimulator(seed=42)
        trips_df, _ = sim.simulate(
            n_drivers=20,
            trips_per_driver=10,
            min_trip_duration_s=60,
            max_trip_duration_s=300,
        )
        trip_features = extract_trip_features(clean_trips(trips_df))
        driver_agg = aggregate_to_driver(trip_features)

        # Simulate near-miss data for the same drivers
        nm_sim = NearMissSimulator(n_groups=2, seed=42)
        # Use same driver IDs as trip data
        driver_ids_from_trips = driver_agg["driver_id"].to_list()
        weekly = nm_sim.simulate(n_drivers=len(driver_ids_from_trips), n_weeks=10)
        # Remap driver IDs to match trip drivers
        id_map = {
            f"DRV{i:04d}": driver_ids_from_trips[i]
            for i in range(len(driver_ids_from_trips))
        }
        weekly = weekly.with_columns(
            pl.col("driver_id").replace(id_map)
        )

        model = ZIPNearMissModel(n_groups=2, max_iter=20, random_state=0)
        model.fit(weekly)
        zip_features = model.driver_risk_features(weekly)

        # Join should succeed without error and produce correct shape
        combined = driver_agg.join(zip_features, on="driver_id", how="left")
        assert combined.shape[0] == driver_agg.shape[0]
        assert "dominant_group" in combined.columns
        assert "nme_rate_per_km" in combined.columns


