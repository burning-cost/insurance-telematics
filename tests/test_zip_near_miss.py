# 3. Group recovery (approximate — probabilistic test)
# ---------------------------------------------------------------------------


class TestGroupRecovery:
    def test_dominant_group_majority_accuracy(self):
        """
        With well-separated groups, dominant predicted group should agree with
        true group for >60% of drivers (above chance level of 33%).
        """
        sim = NearMissSimulator(n_groups=3, seed=7)
        df = sim.simulate(n_drivers=150, n_weeks=52)

        model = ZIPNearMissModel(n_groups=3, max_iter=50, random_state=7)
        model.fit(df)
        features = model.driver_risk_features(df)

        # Ground truth: one row per driver
        true_groups = (
            df.group_by("driver_id")
            .agg(pl.col("true_group").first())
            .sort("driver_id")
        )
        features_sorted = features.sort("driver_id")

        # Allow for label permutation: find best matching permutation
        from itertools import permutations

        true_g = true_groups["true_group"].to_numpy()
        pred_g = np.array(features_sorted["dominant_group"].to_list())

        best_acc = 0.0
        for perm in permutations(range(3)):
            remapped = np.array([perm[p] for p in pred_g])
            acc = float((remapped == true_g).mean())
            best_acc = max(best_acc, acc)

        assert best_acc > 0.55, (
            f"Best-permutation group recovery accuracy {best_acc:.2%} < 55%. "
            "Groups may not be well-separated enough."
        )


